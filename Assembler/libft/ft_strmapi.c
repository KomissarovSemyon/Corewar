#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	size_t	i;
	char	*new;

	i = 0;
	if (s && f)
	{
		while (s[i])
			i++;
		new = (char *)malloc(sizeof(char) * (i + 1));
	}
	else
		new = NULL;
	if (!new)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
